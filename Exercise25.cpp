#include <iostream>
using namespace std;
int main() {
	int a, b;
	cout << "Enter number a: ";
	cin >> a;
	cout << "Enter number b: ";
	cin >> b;
	cout << "Addition: " << a + b << endl;
	cout << "Subtraction: " << a - b << endl;
	cout << "Multiplication: " << a * b << endl;
	cout << "Division: " << a / b << endl;
	return 0;
}
